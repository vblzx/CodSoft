    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String search = JOptionPane.showInputDialog(this,"Enter the roll number of student to search for their data: ");
        try{
            PreparedStatement stmt = con.prepareStatement("SELECT * FROM records WHERE roll_no=?");
            stmt.setString(1,search);
            ResultSet rs;
            rs = stmt.executeQuery();
            if(rs.next()){
                nameTxt.setText(rs.getString(1));
                rollNoTxt.setText(rs.getString(2));
                gradeTxt.setText(rs.getString(3));
                mobNoTxt.setText(rs.getString(4));
                emailTxt.setText(rs.getString(5));
                
            }
        }
        catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

