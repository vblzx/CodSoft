    private void addRecord() {
        try{
            PreparedStatement stmt = con.prepareStatement("INSERT INTO records VALUES(?,?,?,?,?)");
            stmt.setString(1,nameTxt.getText());
            stmt.setString(2,rollNoTxt.getText());
            stmt.setString(3,gradeTxt.getText());
            stmt.setString(4,mobNoTxt.getText());
            stmt.setString(5,emailTxt.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this,"Added student record to database!");
            reset();
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }

    private void reset() {
        nameTxt.setText("");
        rollNoTxt.setText("");
        gradeTxt.setText("");
        mobNoTxt.setText("");
        emailTxt.setText("");
        
    }

